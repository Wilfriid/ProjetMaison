  document.getElementById("openPopup").addEventListener("click", function(event) {
    event.preventDefault();
    document.getElementById("popup").style.display = "block";
  });
  
  document.querySelector(".close").addEventListener("click", function() {
    document.getElementById("popup").style.display = "none";
  });
  // 
  function changerTexte1() {
    var bouton = document.getElementById("bouton-plus-moins1");
    var texteCache = document.getElementById("texte-cache1");
    if (bouton.textContent === "+") {
      bouton.textContent = "-";
      texteCache.style.display = "block";
    } else {
      bouton.textContent = "+";
      texteCache.style.display = "none";
    }
  }
